package br.com.lowcodesystem.dao;

/**
 *
 * @author Maykon
 */
public class Column {

    public String name;
    public int type;

    public Column() {
    }

    public Column(String name, int type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

}
